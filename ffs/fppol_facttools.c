static void uint64_cube(uint64_t * rh, uint64_t * rm, uint64_t * rl,
        const uint64_t * p)
{
    uint64_t rr, c, pp = p[0];
    rr  = Cub[ pp      & 255];
    rr |= Cub[(pp>>8)  & 255]<<24;
    c   = Cub[(pp>>16) & 255];
    rl[0] = rr | (c<<48);  // two first bytes are for the first limb.
    rr  = c>>16;
    rr |= Cub[(pp>>24) & 255]<<8;
    rr |= Cub[(pp>>32) & 255]<<32;
    c   = Cub[(pp>>40) & 255];
    rm[0] = rr | (c<<56);
    rr  = c>>8;
    rr |= Cub[(pp>>48) & 255]<<16;
    rr |= Cub[(pp>>56) & 255]<<40;
    rh[0] = rr;
}

static void fppol64_cube(fppol64_ptr rh, fppol64_ptr rm, fppol64_ptr rl,
        fppol64_srcptr p)
{
    uint64_cube(&rh[0], &rm[0], &rl[0], &p[0]);
    uint64_cube(&rh[1], &rm[1], &rl[1], &p[1]);
}

static void fppol_cube(fppol_ptr r, fppol_srcptr p)
{
    if (fppol_is_zero(p)) {
        fppol_set_zero(r);
        return;
    }
    fppol64_t rl, rm, rh;
    __fppol_realloc_lazy(r, 3*((1+(p->deg>>6))<<6));
    for (int k = p->deg>>6; k>=0; --k) {
        fppol64_cube(rh, rm, rl, p->limbs[k]);
        fppol64_set(r->limbs[3*k], rl);
        fppol64_set(r->limbs[3*k+1], rm);
        fppol64_set(r->limbs[3*k+2], rh);
    }
    r->deg = 3*p->deg;
}

